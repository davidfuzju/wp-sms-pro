<?php

namespace WP_SMS\Gateway;

class bulksmshyderabad extends \WP_SMS\Gateway
{
    private $wsdl_link = "http://tra.bulksmshyderabad.co.in/";
    public $tariff = "http://bulksmshyderabad.co.in/";
    public $unitrial = \false;
    public $unit;
    public $flash = "enable";
    public $isflash = \false;
    public function __construct()
    {
        parent::__construct();
        $this->validateNumber = "981XXXXXXX";
        $this->has_key = \false;
    }
    public function SendSMS()
    {
        /**
         * Modify sender number
         *
         * @param string $this ->from sender number.
         * @since 3.4
         *
         */
        $this->from = apply_filters('wp_sms_from', $this->from);
        /**
         * Modify Receiver number
         *
         * @param array $this ->to receiver number
         * @since 3.4
         *
         */
        $this->to = apply_filters('wp_sms_to', $this->to);
        /**
         * Modify text message
         *
         * @param string $this ->msg text message.
         * @since 3.4
         *
         */
        $this->msg = apply_filters('wp_sms_msg', $this->msg);
        // Get the credit.
        $credit = $this->GetCredit();
        // Check gateway credit
        if (is_wp_error($credit)) {
            // Log the result
            $this->log($this->from, $this->msg, $this->to, $credit->get_error_message(), 'error');
            return $credit;
        }
        $to = \implode(',', $this->to);
        $msg = \urlencode($this->msg);
        //$result = file_get_contents("{$this->wsdl_link}pushsms.php?username={$this->username}&password={$this->password}&message={$msg}&sender={$this->from}&numbers={$to}");
        $response = wp_remote_get(add_query_arg(['userid' => $this->username, 'password' => $this->password, 'sender' => $this->from, 'mobileno' => $to, 'msg' => $msg], \sprintf('%swebsms/sendsms.aspx', $this->wsdl_link)));
        // Check gateway credit
        if (is_wp_error($response)) {
            // Log the result
            $this->log($this->from, $this->msg, $this->to, $response->get_error_message(), 'error');
            return new \WP_Error('send-sms', $response->get_error_message());
        }
        $response_code = wp_remote_retrieve_response_code($response);
        if ($response_code == '200') {
            $result = $response['body'];
            // Log the result
            $this->log($this->from, $this->msg, $this->to, $result);
            /**
             * Run hook after send sms.
             *
             * @param string $result result output.
             * @since 2.4
             *
             */
            do_action('wp_sms_send', $result);
            return $result;
        } else {
            // Log the result
            $this->log($this->from, $this->msg, $this->to, $response['body'], 'error');
            return new \WP_Error('send-sms', $response['body']);
        }
    }
    public function GetCredit()
    {
        // Check username and password
        if (!$this->username or !$this->password) {
            return new \WP_Error('account-credit', __('The username/password for this gateway is not set', 'wp-sms-pro'));
        }
        return \true;
    }
}
